/*--------------------------------------*\
 |*		Public			*|
 \*-------------------------------------*/

bool isPiOMPforAtomic_Ok(int n)
    {
    return isAlgoPI_OK(piOMPforAtomic, n, "Pi OMP for atomic");
    }

/*--------------------------------------*\
 |*		Private			*|
 \*-------------------------------------*/

/**
 * synchronisation couteuse!
 */
double piOMPforAtomic(int n)
    {
    double somme = 0;
    double dx = 1.0 / n;
    double x;
#pragma omp parallel for private (x)
    for (int i = 0; i < n; i++)
	{
	x = i * dx;
#pragma omp atomic
	somme += fpi(x);

	}

    return somme / n;
    }

